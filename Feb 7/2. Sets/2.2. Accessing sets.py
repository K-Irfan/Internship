#We cannot access the values in a set using the index or  key values
#We can  use for loop or ask if a value is present in the set by using the 'in' keyword

#Using for loop
interns={'Irfan','Anusha','Sulaiman'}
for i in interns:
    print(i)
print("\n")

#Checking if the value is present in the set
print('Irfan' in interns,"\n")

#Checking if the value is not present in the set
print('Hi' not in interns,"\n")